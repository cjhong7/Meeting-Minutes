/**
 * ai/prompt.js — AI 프롬프트 생성
 *
 * 학교 협의록 양식 규칙 + 안건 중심 구조화.
 * 3엔진 공통, 모드별 변수(type/section1/section2) 주입.
 */

import { todayIso } from '../state.js';

const MODE_VARS = {
  meeting: {
    type:     '회의록',
    section1: '결정 및 논의 사항',
    section2: '전달 사항',
  },
  plan: {
    type:     '계획안',
    section1: '주요 안건',
    section2: '안내 사항',
  },
};

/**
 * @param {Object} params
 * @returns {{ system: string, user: string }}
 */
export function buildPrompt({ text, mode = 'typing', title = '', date = '', agendas = [], model = '' }) {
  const isPlanning = mode === 'plan';
  const vars = isPlanning ? MODE_VARS.plan : MODE_VARS.meeting;

  // 모든 모드에서 기본적으로 section2(전달사항/안내사항)를 생성하지 않음
  // 사용자가 특별히 요청할 때만 포함
  const showSection2 = false;

  const titleLabel = title.trim() || vars.type;
  const dateStr    = formatDate(date || todayIso());

  // 유효한 안건 목록
  const validAgendas = agendas.filter(a => a?.trim());
  const hasAgendas   = validAgendas.length > 0;

  // 안건 기반 출력 구조 예시 생성
  const agendaExample = hasAgendas
    ? validAgendas.map((a, i) => `${i + 1}. ${a}\n  가. (이 안건에 해당하는 논의·결정 내용 정리)\n  나. (세부 사항)`).join('\n')
    : '1. 안건 제목\n  가. 세부 논의 내용\n    - 구체적 사항\n  나. 결정 사항';

  const system = `당신은 대한민국 초·중·고등학교 협의록 작성 전문가입니다.
아래 [회의 내용]을 학교 공문서 양식에 맞는 정식 협의록으로 작성해 주세요.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[★ 가장 중요한 규칙]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
${hasAgendas
  ? `이 회의의 안건은 다음과 같습니다:
${validAgendas.map((a, i) => `  ${i + 1}. ${a}`).join('\n')}

반드시 위 안건 제목을 [${vars.section1}]의 1단계 번호(1. 2. 3.)로 사용하고,
각 안건 아래에 해당하는 회의 내용을 가. 나. 다. 로 분류·정리해 주세요.${showSection2 ? `
회의 내용 중 안건과 직접 관련 없는 전달·공지 사항은 [${vars.section2}]로 분리하세요.` : ''}`
  : `회의 내용에서 주요 안건을 파악하여 [${vars.section1}]에 번호별로 정리해 주세요.${showSection2 ? `
전달·공지 사항은 [${vars.section2}]로 분리해 주세요.` : ''}`}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[출력 양식]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

■ ${vars.type} ${titleLabel} (${dateStr})

[${vars.section1}]
${agendaExample}
${showSection2 ? `
[${vars.section2}]
1. 전달 항목
  가. 세부 내용` : ''}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[작성 규칙]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

1. 안건 중심 구조화
   - [${vars.section1}]의 1단계 번호(1. 2. 3.)는 반드시 안건 제목으로 사용
   - 각 안건 아래 관련 논의·결정 내용을 가. 나. 다.로 세분화${showSection2 ? `
   - 안건과 무관한 전달 사항은 [${vars.section2}]로 분리` : ''}
   - 안건이 1개뿐이면 1번 하나만 작성

2. 번호 체계 (개조식 3단계)
   - 1단계: 1. 2. 3. (안건 제목)
   - 2단계: 가. 나. 다. (2칸 들여쓰기, 세부 내용)
   - 3단계: - (4칸 들여쓰기, 구체적 사항)

3. 문체
   - 명사형 어미: ~함, ~임, ~됨, ~있음, ~예정임, ~필요함
   - 예) "급식 메뉴를 다양화하기로 결정함"
   - 구어체·대화체 금지, 간결한 공문서체

4. 분량 및 상세화 규칙 (★★ 충실하고 상세하게)
   - 회의 내용을 바탕으로 학교 협의록답게 충실하고 상세하게 작성할 것
   - 각 안건마다 가.나.다. 세부 항목을 5개 이내로 작성
   - 각 논의 사항을 결정 내용·추진 방법·담당자·일정·기대 효과·유의사항
     중 6개 이내의 측면으로 확장하여 항목별로 풀어쓸 것
   - 입력이 간단하더라도 회의 맥락에 맞게 구체적인 실행 내용으로 풀어 작성
   - 학교 협의록에서 통상 다루는 후속 조치·역할 분담을 맥락에 맞게 보완할 것
   - 입력에 있는 날짜·담당자·기한·장소·대상 등은 반드시 포함
   - "~에 대해 논의함" 같은 모호한 표현 대신 구체적 실행 내용으로 기재
   - 같은 안건의 관련 내용은 반드시 해당 안건 번호 아래에 묶기
   - 전체 분량은 A4 한 페이지 분량으로 충실하게 작성할 것

5. 작성 시 유의 (사실 기준 — 환각 주의 ★)
   - 입력에 명시된 핵심 사실(날짜·인명·수치·결정사항)은 임의로 바꾸지 말 것
   - 입력에 없는 새로운 안건·결정·구체적 수치·담당자·기한은 만들어내지 말 것
   - 입력에 적힌 내용을 정리·구조화하는 것을 우선으로 하고,
     맥락상 명백한 후속 조치만 최소한으로 보완할 것
   - 추측성 일반론·배경 설명·통상적 절차의 임의 추가는 하지 말 것
   - 마크다운 기호(###, **, __, \`\`\`) 절대 금지
   - 불필요한 인사말·맺음말 금지
   - 같은 내용 반복 금지`;

  // ── 펜(손글씨 OCR) 모드: 환각 방지 강화 (약간 완화) ──
  const penNote = mode === 'pen'
    ? `

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[펜(손글씨) 모드 특별 지침 ★★ 환각 주의]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
- 입력은 손글씨를 인식한 결과로, 불완전하거나 일부 오인식이 있을 수 있음
- 입력에 적힌 내용을 중심으로 정리하고, 임의의 새 안건·결정은 추가하지 말 것
- 불명확하거나 오인식된 부분은 무리하게 추측하지 말 것
- 다만 적힌 내용을 자연스러운 문장으로 다듬고, 맥락상 자연스러운 후속 조치나
  세부 보완은 적당히 추가하여 협의록답게 정리해도 됨
- 인식된 핵심 단어·표현을 최대한 살려 정리하되 너무 빈약하지 않게 작성할 것`
    : '';

  // ── 단일 안건(또는 안건 없음): 한 장 분량으로 간략하게 ──
  const concise = validAgendas.length <= 1
    ? `

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[분량 조정 — 간략 작성]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
- 안건이 한 개(또는 미지정)이므로 A4 한 장을 넘지 않도록 간결하게 작성
- 세부 항목은 핵심만 3~4개 이내로 추려 작성
- 불필요하게 길게 늘이지 말고 요점 위주로 정리`
    : '';

  // ── 계획서 기반: 소폭 간략 작성 ──
  const planNote = isPlanning ? `

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[계획서 기반 분량 조정]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
- 각 안건의 세부 항목(가.나.다.)은 4개 이내로 작성
- 각 항목의 확장 측면은 4개 이내로 작성
- 핵심 내용은 충실히 담되 불필요한 부연 설명은 생략
- 전체 분량은 타자 기반보다 약간 짧게 유지` : '';

  // ── 모델 등급별 상세도 (pro = 고품질·정교, flash = 간결) ──
  let tierNote = '';
  if (model.includes('2.5-pro')) {
    tierNote = `

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[고품질 작성 지침]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
- 각 안건의 논의·결정 내용을 정교하고 깊이 있게 분석하여 작성
- 표현을 다듬어 전문적이고 완성도 높은 협의록으로 정리
- 각 항목을 구체적이고 명확하게, 빠진 논점 없이 충실하게 작성`;
  } else if (model.includes('2.5-flash')) {
    tierNote = `

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
[간결 작성 지침]
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
- 핵심 위주로 간결하게 요점만 정리`;
  }

  const user = `[회의 내용]\n${text}`;

  return { system: system + penNote + planNote + concise + tierNote, user };
}

function formatDate(iso) {
  if (!iso) return '';
  const [y, m, d] = iso.split('-').map(Number);
  return `${y}. ${m}. ${d}.`;
}
